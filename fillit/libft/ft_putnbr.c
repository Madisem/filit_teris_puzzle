#include "libft.h"

void	ft_putnbr(int n)
{
	long	k;

	k = n;
	if (k < 0)
	{
		ft_putchar('-');
		k = -k;
	}
	if (k > 9)
		ft_putnbr(k / 10);
	ft_putchar(k % 10 + '0');
}
